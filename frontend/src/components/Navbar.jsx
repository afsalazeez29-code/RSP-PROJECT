  const [internalScrolled, setInternalScrolled] = useState(false);
  const [showRecipesDropdown, setShowRecipesDropdown] = useState(false);

  // Check if we're on landing page
  const isLandingPage = location.pathname === '/';

  useEffect(() => {
    const handleResize = () => setWidth(window.innerWidth);
    const handleScroll = () => setInternalScrolled(window.scrollY > 10);

    handleScroll();
    window.addEventListener('resize', handleResize);
    window.addEventListener('scroll', handleScroll);
    return () => {
      window.removeEventListener('resize', handleResize);
      window.removeEventListener('scroll', handleScroll);
    };
  }, []);

  // Close dropdown when clicking outside
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (showRecipesDropdown && !event.target.closest('[data-recipes-dropdown]')) {
        setShowRecipesDropdown(false);
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [showRecipesDropdown]);

  const scrolled = externalScrolled || internalScrolled;

